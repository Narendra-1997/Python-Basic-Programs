# creating class

class Calculate:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def adding(self):
        c = self.a + self.b
        return c

    def substraction(self):
        c = self.a - self.b
        return c

    def division(self):
        c = self.a / self.b
        return c

    def multipulication(self):
        c = self.a * self.b
        return c



